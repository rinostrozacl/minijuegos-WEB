/** Normaliza FIREBASE_PRIVATE_KEY desde env (Coolify, Docker, comillas, \\n). */
export function normalizeFirebasePrivateKey(raw: string): string {
  let key = raw.trim();

  if (
    (key.startsWith('"') && key.endsWith('"')) ||
    (key.startsWith("'") && key.endsWith("'"))
  ) {
    key = key.slice(1, -1);
  }

  return key.replace(/\\n/g, "\n");
}
